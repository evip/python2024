# Tạo danh sách
students = ["Nguyen Van A", "Tran Thi B", "Le Van C"]

# Thêm phần tử vào danh sách
students.append("Pham Thi D")

# Thay đổi giá trị phần tử
students[1] = "Tran Thi E"

# Xóa phần tử khỏi danh sách
students.remove("Nguyen Van A")

# In danh sách
print(students)  # Output: ['Tran Thi E', 'Le Van C', 'Pham Thi D']





